class Solution {
public:
    vector<int> findMaxRow(vector<vector<int>> mat, int n) {
          int k=0;
    
            for(int i=0 ; k<n ; i++)
            {
                if(mat[i][k]==1)
                {
                    return {i,n-k};
                }
                if(i+1==n)
                {
                    k++;
                    i=-1;
                }
            }

        return {0,0};
    }
};

