class Solution {
  public:
    void printTriangle(int n) {
        // code here
        char ch='A';
        for(int row=1;row<=n;row++){
            
            for(int i=1;i<=row;i++){
                cout<<ch;
                
            }
            ch++;
            cout<<endl;
        }
    }
};

