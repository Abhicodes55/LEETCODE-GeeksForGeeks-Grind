class Solution{
    public:
        long long pairWithMaxSum(long long arr[], long long N)
    {
        long long first=0,second=1,sum=0,Maxsum=0;
        while(second<N){
            sum=(arr[first++]+arr[second++]);//Adding two consecutive arr elements
            Maxsum=max(Maxsum,sum);//Finding Max
            sum=0;
        }
        return Maxsum;
    }
};



